#Basic operations
print(2 + 2)
print(2 - 2)
print(2 / 2)
print(2 * 2)
print(5 % 2)
print(10 // 3) #Floor division -> to int
print(5 ** 2)
print("Hello" + "World")
print("Hello" + str(13))
print("Hello" * 3)

#Comparing operations
print(2 == 2)
print(2 > 5)
print(1 < 2)
print(3 != 2)
print(2 >= 2)
print(2 <= 8)

#Logical operators
print(2 > 8 and 3 < 4)
print(2 > 8 or 3 < 4)
print(not(3 > 4))